    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        // TODO add your handling code here:
        try{
            javax.swing.table.DefaultTableModel dtm = (javax.swing.table.DefaultTableModel)category_table.getModel();
            int rc = dtm.getRowCount();
            while(rc-- != 0){
                dtm.removeRow(0);
            }
            java.sql.Date cdf = new java.sql.Date(category_from.getDate().getTime());
            java.sql.Date cdt = new java.sql.Date(category_to.getDate().getTime());
            String category = (String)category_dropdown.getSelectedItem();
            ResultSet rs = database.database_connect.s.executeQuery("select * from expense where expense_category='"+category+"' and expense_date>='"+cdf+"' and expense_date<='"+cdt+"' order by expense_date asc");
            int tot = 0;
            while(rs.next()){
                tot += rs.getInt("amount");
                Object o[] = {rs.getDate("expense_date"), rs.getString("expense_category"), rs.getInt("amount")};
                dtm.addRow(o);
            }
            category_amount.setText(tot+"");
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

