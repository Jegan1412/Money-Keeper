    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try{
            javax.swing.table.DefaultTableModel dtm = (javax.swing.table.DefaultTableModel)date_table.getModel();
            int rc = dtm.getRowCount();
            while(rc-- != 0){
                dtm.removeRow(0);
            }
            java.sql.Date df = new java.sql.Date(date_from.getDate().getTime());
            java.sql.Date dt = new java.sql.Date(date_to.getDate().getTime());
            ResultSet rs = database.database_connect.s.executeQuery("select * from expense where expense_date>='"+df+"' and expense_date<='"+dt+"' order by expense_date asc");
            int tot = 0;
            while(rs.next()){
                tot += rs.getInt("amount");
                Object o[] = {rs.getDate("expense_date"), rs.getString("expense_category"), rs.getInt("amount")};
                dtm.addRow(o);
            }
            date_amount.setText(tot+"");
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

