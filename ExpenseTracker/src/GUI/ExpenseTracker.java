    private void expense_addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_expense_addActionPerformed
        // TODO add your handling code here:
        try{
            java.util.Date dt = date.getDate();
            String amt = amount.getText();
            String category = (String)category_dropdown.getSelectedItem();
            if(dt != null && !amt.equals("") && !category.equals("")){
                java.sql.Date date = new java.sql.Date(dt.getTime());
                int amnt = Integer.parseInt(amt);
                database.database_connect.s.executeUpdate
        ("insert into expendituredb.expense (expense_date,expense_category,amount) values ('"+date+"','"+category+"',"+amnt+")");
                JOptionPane.showMessageDialog(null, "Expense Added Successfully");
                getEntries();
                limitCheck();
            }else{
                JOptionPane.showMessageDialog(null, "Invalid Field Values");
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_expense_addActionPerformed

    private void expense_removeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_expense_removeActionPerformed
        // TODO add your handling code here:
        int ri = jTable1.getSelectedRow();
        if(ri != -1){
            int confirmation = JOptionPane.showConfirmDialog(null, "Do you want to Delete?", "Confirmation of Deletion",JOptionPane.YES_NO_OPTION);
            if(confirmation == JOptionPane.YES_OPTION){
                int id = (int)jTable1.getValueAt(ri, 0);
                try{
                    database.database_connect.s.executeUpdate("delete from expense where expense_id = '"+id+"'");
                    JOptionPane.showMessageDialog(null, "Expense Deleted Successfully");
                    getEntries();
                }catch(Exception e){
                    JOptionPane.showMessageDialog(null, e);
                }
            }
        }
    }//GEN-LAST:event_expense_removeActionPerformed

