    private void display_category(){
        try{
            category_dropdown.removeAllItems();
            ResultSet rs = database.database_connect.s.executeQuery("select * from expendituredb.category_details");
            while(rs.next()){
                category_dropdown.addItem(rs.getString("category"));
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
    private void getEntries(){
        try{
            javax.swing.table.DefaultTableModel dtm = (javax.swing.table.DefaultTableModel)jTable1.getModel();
            int rc = dtm.getRowCount();
            while(rc-- != 0){
                dtm.removeRow(0);
            }
            java.time.LocalDate cd = java.time.LocalDate.now();
            java.time.LocalDate bd = cd.minusDays(20);
            ResultSet rs = database.database_connect.s.executeQuery("select * from expense where expense_date<='"+cd+"' and expense_date>='"+bd+"'");
            int total_amt = 0;
            while(rs.next()){
                total_amt += rs.getInt("amount");
                Object o[] = {rs.getInt("expense_id"), rs.getString("expense_category"), rs.getDate("expense_date"), rs.getInt("amount")};
                dtm.addRow(o);
            }
            total.setText(total_amt+"");
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
    private void limitCheck(){
        try{
            java.time.LocalDate cd = java.time.LocalDate.now();
            ResultSet rs = database.database_connect.s.executeQuery("select * from expense where expense_date='"+cd+"' ");
            int total_spend = 0;
            while(rs.next()){
                total_spend += rs.getInt("amount");
            }
            ResultSet os = database.database_connect.s.executeQuery("select * from other_information where id=1 ");
            int limit_value = 0;
            if(os.next()){
                limit_value = os.getInt("limitValue");
            }
            if(total_spend > limit_value){
                JOptionPane.showMessageDialog(null, "Limit has been Exceeded or Reached");
                int limitBreak_count = os.getInt("Limit_break")+1;
                database.database_connect.s.executeUpdate
        ("update expendituredb.other_information set limit_break="+limitBreak_count+" where id=1 ");
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }
    
