    private void category_deleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_category_deleteActionPerformed
        // TODO add your handling code here:
        int confirmation = JOptionPane.showConfirmDialog(null, "Do you want to Delete?", "Confirmation of Deletion",JOptionPane.YES_NO_OPTION);
        if(confirmation == JOptionPane.YES_OPTION){
            int ri = category_table.getSelectedRow();
            String category = (String) category_table.getValueAt(ri, 1);
            try{
                database.database_connect.s.executeUpdate("delete from expendituredb.category_details where category ='" + category + "'");
                JOptionPane.showMessageDialog(null, "Category Deleted Successfully");
                getEntries();
            } catch(Exception e){
                JOptionPane.showMessageDialog(null, e);
            }
        }
    }//GEN-LAST:event_category_deleteActionPerformed

    private void category_addActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_category_addActionPerformed
        // TODO add your handling code here:
        try{
            String category = category_input.getText();
                if(!category.equals("")){
                database.database_connect.s.executeUpdate("insert into category_details values ('"+category+"')");
                JOptionPane.showMessageDialog(null, "Category Added Successfully!");
                getEntries();
               }else{
                    JOptionPane.showMessageDialog(null, "Please Enter a Valid Category Value");
                }
        } catch(java.sql.SQLIntegrityConstraintViolationException e){
            JOptionPane.showMessageDialog(null, "Category Already Exists");
        } catch(Exception e){
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_category_addActionPerformed

